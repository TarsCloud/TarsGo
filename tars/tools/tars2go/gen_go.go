func (gen *GenGo) genSwitchCaseBody(tname string, fun *FunInfo) {
	c := &gen.code
	c.WriteString(`func ` + fun.NameStr + `(ctx context.Context, _val interface{},_os *codec.Buffer, _is *codec.Reader, withContext bool)(err error){`)
	c.WriteString(`
	var length int32
	var have bool
	var ty byte
	`)

	for k, v := range fun.Args {
		c.WriteString("var " + v.Name + " " + gen.genType(v.Type))
		if !v.IsOut {
			dummy := &StructMember{}
			dummy.Type = v.Type
			dummy.Key = v.Name
			dummy.Tag = int32(k + 1)
			dummy.Require = true
			gen.genReadVar(dummy, "", false)
		} else {
			c.WriteString("\n")
		}
	}

	if fun.HasRet {
		c.WriteString(`if withContext == false {
		_imp := _val.(_imp` + tname + `)
		ret, err := _imp.` + fun.Name + `(`)
		for _, v := range fun.Args {
			if v.IsOut || v.Type.CType == tkStruct {
				c.WriteString("&" + v.Name + ",")
			} else {
				c.WriteString(v.Name + ",")
			}
		}
		c.WriteString(")")
		c.WriteString(`
		if err != nil {
			return err
		}
		`)
		dummy := &StructMember{}
		dummy.Type = fun.RetType
		dummy.Key = "ret"
		dummy.Tag = 0
		dummy.Require = true
		gen.genWriteVar(dummy, "", false)
		c.WriteString("}else{")
		c.WriteString(`
		_imp := _val.(_imp` + tname + `WithContext)
		ret, err := _imp.` + fun.Name + `(ctx ,`)
		for _, v := range fun.Args {
			if v.IsOut || v.Type.CType == tkStruct {
				c.WriteString("&" + v.Name + ",")
			} else {
				c.WriteString(v.Name + ",")
			}
		}
		c.WriteString(")")
		c.WriteString(`
		if err != nil {
			return err
		}
		`)
		dummy = &StructMember{}
		dummy.Type = fun.RetType
		dummy.Key = "ret"
		dummy.Tag = 0
		dummy.Require = true
		gen.genWriteVar(dummy, "", false)
		c.WriteString("}\n")

	} else {
		c.WriteString(`if withContext == false {
		_imp := _val.(_imp` + tname + `)
		err = _imp.` + fun.Name + `(`)
		for _, v := range fun.Args {
			if v.IsOut || v.Type.CType == tkStruct {
				c.WriteString("&" + v.Name + ",")
			} else {
				c.WriteString(v.Name + ",")
			}
		}
		c.WriteString(")")
		c.WriteString(`
		if err != nil {
			return err
		}
		`)
		c.WriteString("}else{")
		c.WriteString(`
		_imp := _val.(_imp` + tname + `WithContext)
		err = _imp.` + fun.Name + `(ctx ,`)
		for _, v := range fun.Args {
			if v.IsOut || v.Type.CType == tkStruct {
				c.WriteString("&" + v.Name + ",")
			} else {
				c.WriteString(v.Name + ",")
			}
		}
		c.WriteString(")")
		c.WriteString(`
		if err != nil {
			return err
		}
		`)
		c.WriteString("}\n")
	}

	for k, v := range fun.Args {
		if v.IsOut {
			dummy := &StructMember{}
			dummy.Type = v.Type
			dummy.Key = v.Name
			dummy.Tag = int32(k + 1)
			dummy.Require = true
			gen.genWriteVar(dummy, "", false)
		}
	}
	c.WriteString(`
_ = length
_ = have
_ = ty
`)
	c.WriteString(`return nil
	}`)
	c.WriteString("\n")

}
func (gen *GenGo) genIFDispatch(itf *InterfaceInfo) {
	c := &gen.code
	for _, v := range itf.Fun {
		gen.genSwitchCaseBody(itf.TName, &v)
	}
	c.WriteString("//Dispatch is used to call the server side implement for the method defined in the tars file. withContext shows using context or not.  \n")
	c.WriteString("func(_obj *" + itf.TName + `) Dispatch(ctx context.Context, _val interface{}, req *requestf.RequestPacket, resp *requestf.ResponsePacket,withContext bool) (err error) {
  `)

	var param bool
	for _, v := range itf.Fun {
		if len(v.Args) > 0 {
			param = true
			break
		}
	}

	if param {
		c.WriteString("_is := codec.NewReader(tools.Int8ToByte(req.SBuffer))")
	} else {
		c.WriteString("_is := codec.NewReader(nil)")
	}
	c.WriteString(`
_os := codec.NewBuffer()
switch req.SFuncName {
`)

	for _, v := range itf.Fun {
		gen.genSwitchCase(itf.TName, &v)
	}

	c.WriteString(`
default:
	return fmt.Errorf("func mismatch")
}
var _status map[string]string
s, ok := current.GetResponseStatus(ctx)
if ok  && s != nil {
	_status = s
}
var _context map[string]string
c, ok := current.GetResponseContext(ctx)
if ok && c != nil  {
	_context = c
}
*resp = requestf.ResponsePacket{
	IVersion:     1,
	CPacketType:  0,
	IRequestId:   req.IRequestId,
	IMessageType: 0,
	IRet:         0,
	SBuffer:      tools.ByteToInt8(_os.ToBytes()),
	Status:       _status,
	SResultDesc:  "",
	Context:      _context,
}
return nil
}
`)
}

